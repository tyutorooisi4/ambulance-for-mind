                <h4><a id="stroke">手を使って身体をなでたり、トントンしたりする</a></h4>
                <p>自分の手をつかって、体のどこかをやさしくなでたり、トントンと軽くタッチしたりしてみましょう。
                なでたりタッチしたりするのは、どこでも構いません。<br>
                できれば目を閉じて、自分の手の重さや暖かさ、タッチの優しい刺激を身体で感じながら続けてみましょう。<br>
                </p>
                <h4><a id="comeback">傷つけずに、身体に強めの刺激を与える</a></h4>
                <p>リストカットなどの自傷行為以外の方法で、身体に強めの刺激を与えてみましょう。<br>
                手の甲や腕をつねる、氷水に両手をつけるなど、やり方はなんでも構いません。<br>
                刺激による身体の感覚に注意を向け、集中することによって、我に返ることができます。<br>
                エスカレートして自傷行為に発展しそうであれば、すぐにこのワークを中止し、<br>
                他の方法を試すようにしてください。<br>
                </p>
                <h4><a id="blanket">大きな布やストールや毛布にくるまれる</a></h4>
                <p>なにかやわらかくて大きなものにくるまれてみましょう。<br>
                椅子に座って布を頭や肩にかけてもいいし、毛布のなかに潜り込むのも良いでしょう。<br>
                カーテンを体に巻き付けることもできます。<br>
                </p>
                <h4><a id="breath">大げさに息を吐きまくる</a></h4>
                <p>ため息は、心身の緊張や疲れを取るための身体の知恵でもあります。<br>
                ぜひ、5回でも10回でも、大げさなため息を繰り返してみてください。<br>
                </p>
                <h4><a id="hug">何かをギューッと抱きしめる</a></h4>
                <p>抱きしめるという行為自体に、人を落ち着かせる大きな効果があります。<br>
                家族やパートナー、友人、あるいはペットやぬいぐるみをハグしてみましょう。<br>
                他にも、自分の体に腕を回して自分で自分を抱きしめたり、クッションや枕をハグすることもできます。<br>
                </p>
